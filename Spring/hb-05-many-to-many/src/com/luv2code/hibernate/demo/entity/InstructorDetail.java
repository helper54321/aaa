package com.luv2code.hibernate.demo.entity;

import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.OneToOne;
import javax.persistence.Table;

@Entity
@Table(name="instructor_detail")
public class InstructorDetail {

	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	@Column(name="id")
	private int id;
	
	@Column(name="youtube_channel")
	private String youtubeChannel;
	
	@Column(name="hobby")
	private String hobby;
	
	//új mezõ az instructor eléréséhez (ezt az adatbázisba nem kell felvinni)
	//alapból, ahogy a hb-01-es projektnél csináltuk elõször le kellett kérni az instructort, és azon keresztül tudtuk elérni a hozzátartozó instructordetailt
	//ezzel a kiegészítéssel visszafelé is mûködni fog (ezért bi-directional)
	//A mappedby-nál az Insturctor classben lévõ foreign keyként használt változót adjuk meg (mivel annál elmondjuk, hogy hogyan kösse össze a táblákat, így hogy hivatkozunk arra, lehetõségünk lesz visszafelé is a lekérésre ahogy fentebb írtam)
	
//	@OneToOne(mappedBy = "instructorDetail", cascade = CascadeType.ALL)
	//Ebben az esetben all helyett módosítottunk, így egyedül a törlés esetén (mivel azt kihagytuk a felsorolásból) csak egyirányú lesz a mûvelet. A hozzákapcsolódó instructor nem fog törlõdni
	@OneToOne(mappedBy = "instructorDetail", 
			cascade = {CascadeType.DETACH, CascadeType.MERGE, CascadeType.PERSIST, CascadeType.REFRESH})
	private Instructor instructor;
	
	
	public InstructorDetail() {
		
	}

	public InstructorDetail(String youtubeChannel, String hobby) {
		this.youtubeChannel = youtubeChannel;
		this.hobby = hobby;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getYoutubeChannel() {
		return youtubeChannel;
	}

	public void setYoutubeChannel(String youtubeChannel) {
		this.youtubeChannel = youtubeChannel;
	}

	public String getHobby() {
		return hobby;
	}

	public void setHobby(String hobby) {
		this.hobby = hobby;
	}
	
	public Instructor getInstructor() {
		return instructor;
	}

	public void setInstructor(Instructor instructor) {
		this.instructor = instructor;
	}

	@Override
	public String toString() {
		return "InstructorDetail [id=" + id + ", youtubeChannel=" + youtubeChannel + ", hobby=" + hobby + "]";
	}
	
	
	
	
	
}
